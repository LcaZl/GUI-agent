class OsworldSettings(BaseModel):
    """
    Client + environment settings for OSWorld/DesktopEnv.
    Matches the YAML structure; most fields are required, no extras allowed.
    """
    # --- environment_settings (YAML) ---
    provider_name: Literal["vmware"] = Field(..., description="Virtualization provider name")
    path_to_vm: Path = Field(..., description="Path to the .vmx file of the VMware VM")
    snapshot_name: str = Field(..., description="Snapshot name to restore (e.g., 'init_state')")
    headless: bool = Field(..., description="Run the VM without GUI")
    require_a11y_tree: bool = Field(..., description="Whether the accessibility tree is required")
    require_terminal: bool = Field(..., description="Whether a terminal is required")
    enable_proxy : bool = Field(..., description = "")
    os_type: str = Field(..., description="Guest OS type (e.g., 'Ubuntu')")
    action_space : str = Field(..., description="Action Space")
    socket_timeout_sec: float = Field(
        default=120.0,
        gt=0.0,
        description="Socket timeout (sec) used by OSWorld TCP client calls.",
    )
    init_timeout_sec: float = Field(
        default=180.0,
        gt=0.0,
        description="Maximum seconds to wait for OSWorld environment readiness at startup.",
    )
    # --- optional auth (YAML may omit) ---
    client_username: Optional[str] = Field(None, description="Username for the DesktopEnv client")
    client_password: Optional[str] = Field(None, description="Password for the DesktopEnv client")

    # --- validators ---
    @field_validator("path_to_vm", mode="before")
    @classmethod
    def validate_vmx_path(cls, v):
        """
        Validate vmx path.
        
        Parameters
        ----------
        v : Any
            Function argument.
        
        Returns
        -------
        Any
            Function result.
        
        """
        p = Path(v)
        if p.suffix.lower() != ".vmx":
            raise ValueError("path_to_vm must point to a .vmx file")
        return p

    model_config = ConfigDict(extra="forbid", validate_assignment=True)


